namespace XUnity.ResourceRedirector
{
   /// <summary>
   /// Enum representing the source of an asset being loaded.
   /// </summary>
   public enum AssetSource
   {
      /// <summary>
      /// The source is the UnityEngine.AssetBundle class.
      /// </summary>
      AssetBundle = 1,

      /// <summary>
      /// The source is the UnityEngine.Resources class.
      /// </summary>
      Resources = 2
   }
}
